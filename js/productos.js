const collage = document.getElementById("collage");

function createCard(element) {
	const cardElement = document.createElement("div");

	cardElement.setAttribute("class", "col-lg-4 col-md-12 col-sm-12 imagen");

	cardElement.innerHTML = `
              <div class="product-card position-relative pe-3 pb-3" role="button" onclick="openModal('{element}')" data-bs-target="#modaltoggle" data-bs-toggle="modal">
                <div class="image-holder zoom-effect">
                  <img src="{element}"
                    alt="product-item" class="img-fluid rounded">
                </div>

                <div class="cart-detail d-flex justify-content-between align-items-center">
                  <h3 class="card-title fs-6 pt-3">
                    <a href="#">Running shoes for men</a>
                  </h3>
                  <span class="card-price fw-bold">$99</span>
                </div>
              </div>
            `.replace(/{element}/g, element);

	collage.appendChild(cardElement);
}

